

function PlanUno(){

    let cant = document.getElementById('cantidad').value;
    const precio= 499;
  
     let plan = precio * cant;

             Swal.fire({
        title: 'CLARO',
        text:` Evalor mensual es${plan} ` ,
        imageUrl: 'https://1000marcas.net/wp-content/uploads/2021/02/Claro-Logo-2004-500x284.png',
        imageWidth: 400,
        imageHeight: 200,
        imageAlt: 'Custom image',
      })


 Document.getElementById('cantidad').value ="";


}



